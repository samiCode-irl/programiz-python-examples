# Python Program to Check if a Number is Positive, Negative or 0
# In this example, you will learn to check whether a number entered by the user is positive,
# negative or zero. This problem is solved using if...elif...else and nested if...else statement.

num = float(input('Enter the number: '))

if num > 0:
    print('positive')
elif num < 0:
    print('negative')
else:
    print(0)
